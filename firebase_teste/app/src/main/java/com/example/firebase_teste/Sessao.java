package com.example.firebase_teste;

public class Sessao {
    private Integer rad, data, duracao, fugas, perigo;

    public Sessao() {
    }

    public Integer getRad() {
        return rad;
    }

    public void setRad(Integer rad) {
        this.rad = rad;
    }

    public Integer getData() {
        return data;
    }

    public void setData(Integer data) {
        this.data = data;
    }

    public Integer getDuracao() {
        return duracao;
    }

    public void setDuracao(Integer duracao) {
        this.duracao = duracao;
    }

    public Integer getFugas() {
        return fugas;
    }

    public void setFugas(Integer fugas) {
        this.fugas = fugas;
    }

    public Integer getPerigo() {
        return perigo;
    }

    public void setPerigo(Integer perigo) {
        this.perigo = perigo;
    }
}
